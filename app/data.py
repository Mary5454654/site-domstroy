def get_article(slug):
    for article in ARTICLES:
        if article["slug"] == slug:
            return article
    return None


def searchable_items():
    items = []
    for project in PROJECTS:
        items.append(
            {
                "type": "Проект",
                "title": project["title"],
                "text": f"{project['area']} {project['material']} {project['price']}",
                "endpoint": "main.projects",
            }
        )
    for service in SERVICES:
        items.append(
            {
                "type": "Услуга",
                "title": service["title"],
                "text": service["text"],
                "endpoint": "main.services",
            }
        )
    for article in ARTICLES:
        items.append(
            {
                "type": "Статья",
                "title": article["title"],
                "text": article["summary"] + " " + " ".join(article["body"]),
                "endpoint": "main.article_detail",
                "slug": article["slug"],
            }
        )
    for item in NEWS:
        items.append(
            {
                "type": "Новость",
                "title": item["title"],
                "text": item["text"],
                "endpoint": "main.news",
            }
        )
    return items
